// ── Snake/Camel key mapping ──────────────────────────────────────
const S2C_SPECIAL = { ride_days_since_last_pm: "rideDaysSinceLastPM", last_pm_date: "lastPMDate", legacy_id: "_legacyId" };
const C2S_SPECIAL = { rideDaysSinceLastPM: "ride_days_since_last_pm", lastPMDate: "last_pm_date", _legacyId: "legacy_id" };
const SKIP_KEYS = new Set(["_schemaVersion", "_legacyMaintenance", "_autoGenerated", "_bookingId"]);

function snakeToCamel(s) { return S2C_SPECIAL[s] || s.replace(/_([a-z])/g, (_, c) => c.toUpperCase()); }
function camelToSnake(s) { return C2S_SPECIAL[s] || s.replace(/[A-Z]/g, c => "_" + c.toLowerCase()); }
function rowToJs(row) { return Object.fromEntries(Object.entries(row).map(([k, v]) => [snakeToCamel(k), v])); }
function jsToRow(obj) { return Object.fromEntries(Object.entries(obj).filter(([k]) => !SKIP_KEYS.has(k)).map(([k, v]) => [camelToSnake(k), v])); }

