// Schema version: bump this when defaultDB structure changes to trigger migration
export const DB_SCHEMA_VERSION = 6; // v1 = original, v2 = Phase 0 data model, v3 = auth PINs, v4 = client user accounts, v5 = bookings, v6 = tool location tracking

export function migrateDB(stored) {
  const migrated = { ...stored };
  // Ensure all top-level collections exist
  if (!migrated.users) migrated.users = defaultDB.users;
  if (!migrated.parts) migrated.parts = defaultDB.parts;
  if (!migrated.supplies) migrated.supplies = defaultDB.supplies;
  if (!migrated.workOrders) migrated.workOrders = defaultDB.workOrders;
  if (!migrated.purchaseRequests) migrated.purchaseRequests = [];
  // Preserve original maintenance as legacy backup if not already done
  if (migrated.maintenance && !migrated._legacyMaintenance) {
    migrated._legacyMaintenance = JSON.parse(JSON.stringify(migrated.maintenance));
  }
  // Extend existing bikes with new PM/booking fields
  if (migrated.bicycles) {
    const defaultBikeFields = { ownership: "company", pmIntervalDays: 60, pmIntervalRideDays: 15, rideDaysSinceLastPM: 0, lastPMDate: null, suspensionSettings: null, qrCode: null, bookable: false, currentRiderId: null };
    migrated.bicycles = migrated.bicycles.map(bike => {
      const merged = { ...defaultBikeFields, ...bike };
      // Backfill lastPMDate from defaultDB if available
      if (!merged.lastPMDate) {
        const defaultBike = defaultDB.bicycles.find(b => b.id === bike.id);
        if (defaultBike?.lastPMDate) merged.lastPMDate = defaultBike.lastPMDate;
      }
      return merged;
    });
  }
  // Extend existing clients with new communication/property fields
  if (migrated.clients) {
    const defaultClientFields = { contactName: "", contactEmail: "", contactPhone: "", communicationPref: "silent", accessInstructions: "", toolsOnSite: [], toolOwnership: "client" };
    migrated.clients = migrated.clients.map(client => ({ ...defaultClientFields, ...client }));
  }
  // v3: Add PIN field to users for auth
  if (migrated.users) {
    const defaultPins = { u1: "1111", u2: "2222", u3: "3333", u4: "4444", u5: "5555", u6: "6666" };
    migrated.users = migrated.users.map(u => {
      if (!u.pin) return { ...u, pin: defaultPins[u.id] || "0000" };
      return u;
    });
  }
  // v4: Add client user accounts (one per property)
  if (migrated.users) {
    const clientUserIds = ["u4", "u5", "u6"];
    const existingIds = new Set(migrated.users.map(u => u.id));
    const clientDefaults = defaultDB.users.filter(u => clientUserIds.includes(u.id));
    clientDefaults.forEach(cu => {
      if (!existingIds.has(cu.id)) migrated.users.push({ ...cu });
    });
  }
  // v5: Add bookings collection
  if (!migrated.bookings) migrated.bookings = [];
  // v6: Tool location tracking
  if (!migrated.toolLocationLog) migrated.toolLocationLog = [];
  if (migrated.tools) {
    migrated.tools = migrated.tools.map(t => ({
      currentPropertyId: null,
      checkedOutBy: null,
      ...t,
    }));
  }
  migrated._schemaVersion = DB_SCHEMA_VERSION;
  return migrated;
}

// ── Sync load from localStorage (instant, offline-first) ─────────
export function loadDB() {
  try {
    const raw = localStorage.getItem(DB_KEY);
    if (raw) {
      const stored = JSON.parse(raw);
      if (!stored._schemaVersion || stored._schemaVersion < DB_SCHEMA_VERSION) {
        const migrated = migrateDB(stored);
        localStorage.setItem(DB_KEY, JSON.stringify(migrated));
        return migrated;
      }
      _lastSaved = stored;
      return stored;
    }
  } catch (e) { console.error("DB load error", e); }
  const fresh = { ...defaultDB, _schemaVersion: DB_SCHEMA_VERSION };
  _lastSaved = fresh;
  return fresh;
}

// ── Async fetch from Supabase (hydrates after initial render) ────
export async function fetchDB() {
  const results = await Promise.all(
    Object.entries(COLLECTIONS).map(([, table]) =>
      supabase.from(table).select("*")
    )
  );
  const tripResult = await supabase.from("grant_trip").select("data").eq("id", 1).single();

  const keys = Object.keys(COLLECTIONS);
  const db = { _schemaVersion: DB_SCHEMA_VERSION };
  keys.forEach((jsKey, i) => {
    db[jsKey] = (results[i].data || []).map(rowToJs);
  });
  db.grantTrip = tripResult.data?.data || defaultDB.grantTrip;

  // Cache locally
  localStorage.setItem(DB_KEY, JSON.stringify(db));
  _lastSaved = db;
  return db;
}

// ── Save: localStorage (sync) + Supabase (async background) ─────
export function saveDB(db) {
  localStorage.setItem(DB_KEY, JSON.stringify(db));
  _syncToSupabase(db);
}

async function _syncToSupabase(db) {
  if (_syncInProgress) return;

  // Don't attempt sync if not authenticated — mark dirty for later retry
  const { data: { session } } = await supabase.auth.getSession();
  if (!session) {
    for (const [jsKey] of Object.entries(COLLECTIONS)) {
      if (!_lastSaved || db[jsKey] !== _lastSaved[jsKey]) _dirtyCollections.add(jsKey);
    }
    if (!_lastSaved || db.grantTrip !== _lastSaved.grantTrip) _dirtyCollections.add("_grantTrip");
    _notifySyncStatus();
    return;
  }

  _syncInProgress = true;
  _notifySyncStatus();

  // Determine collections to sync: changed ones + previously failed ones
  const toSync = new Set(_dirtyCollections);
  for (const [jsKey] of Object.entries(COLLECTIONS)) {
    if (!_lastSaved || db[jsKey] !== _lastSaved[jsKey]) toSync.add(jsKey);
  }
  if (!_lastSaved || db.grantTrip !== _lastSaved.grantTrip) toSync.add("_grantTrip");

  const baseSnapshot = _lastSaved;
  const succeeded = new Set();

  try {
    for (const [jsKey, tableName] of Object.entries(COLLECTIONS)) {
      if (!toSync.has(jsKey)) continue;

      const rows = (db[jsKey] || []).map(jsToRow);
      const newIds = new Set((db[jsKey] || []).map(i => i.id));
      const oldIds = new Set((baseSnapshot?.[jsKey] || []).map(i => i.id));
      let failed = false;

      if (rows.length > 0) {
        const { error } = await supabase.from(tableName).upsert(rows, { onConflict: "id" });
        if (error) { console.error(`Supabase upsert ${tableName}: ${error.message} (${error.code})`); failed = true; }
      }

      if (!failed) {
        const deletedIds = [...oldIds].filter(id => !newIds.has(id));
        if (deletedIds.length > 0) {
          const { error } = await supabase.from(tableName).delete().in("id", deletedIds);
          if (error) { console.error(`Supabase delete ${tableName}: ${error.message} (${error.code})`); failed = true; }
        }
      }

      if (failed) _dirtyCollections.add(jsKey);
      else { _dirtyCollections.delete(jsKey); succeeded.add(jsKey); }
    }

    // Singleton grant_trip
    if (toSync.has("_grantTrip")) {
      const { error } = await supabase.from("grant_trip").upsert({ id: 1, data: db.grantTrip });
      if (error) { console.error(`Supabase grant_trip sync: ${error.message} (${error.code})`); _dirtyCollections.add("_grantTrip"); }
      else { _dirtyCollections.delete("_grantTrip"); succeeded.add("_grantTrip"); }
    }
  } catch (e) {
    // Network completely down — mark all attempted as dirty
    for (const key of toSync) _dirtyCollections.add(key);
    console.error("Supabase sync error:", e);
  }

  // Only update _lastSaved for collections that succeeded
  if (succeeded.size > 0) {
    const merged = { ...(_lastSaved || db) };
    for (const key of succeeded) {
      if (key === "_grantTrip") merged.grantTrip = db.grantTrip;
      else merged[key] = db[key];
    }
    merged._schemaVersion = db._schemaVersion;
    _lastSaved = merged;
  } else if (!baseSnapshot && _dirtyCollections.size === 0) {
    _lastSaved = db;
  }

  _syncInProgress = false;
  _notifySyncStatus();
}

// ── Sync retry (called on reconnection) ─────────────────────────
export async function retrySyncNow() {
  if (_dirtyCollections.size === 0) return;
  const currentDb = loadDB();
  await _syncToSupabase(currentDb);
}

export function getSyncPendingCount() {
  return _dirtyCollections.size;
}

