// ── Sync status pub/sub ─────────────────────────────────────────
export function onSyncStatusChange(callback) {
  _syncListeners.push(callback);
  return () => { _syncListeners = _syncListeners.filter(cb => cb !== callback); };
}

function _notifySyncStatus() {
  const status = { pendingCount: _dirtyCollections.size, syncing: _syncInProgress };
  _syncListeners.forEach(cb => cb(status));
}

